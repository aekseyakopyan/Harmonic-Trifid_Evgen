def _normalize_text(text: str) -> str:
    """Нормализует Unicode-омоглифы и диакритику для надёжного матчинга."""
    text = unicodedata.normalize('NFC', text)
    text = text.translate(_UNICODE_HOMOGLYPHS)
    return text

