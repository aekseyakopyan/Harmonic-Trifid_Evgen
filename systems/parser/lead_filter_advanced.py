def _has_negation(text_lower: str, keyword: str) -> bool:
    """Проверяет, не стоит ли ключевое слово в контексте отрицания/владения."""
    kw_escaped = re.escape(keyword)
    for pattern in _NEGATION_PATTERNS:
        if re.search(pattern.format(kw=kw_escaped), text_lower):
            return True
    return False


def _score_keyword(text_lower: str, keyword: str) -> float:
    """
    Возвращает вес ключевика с учётом контекста:
      0   — ключ отсутствует
     -10  — ключ есть, но в контексте отрицания/владения
      1.0 — нейтральное упоминание
      2.5 — рядом (±80 символов) есть сигнал запроса
    """
    if keyword not in text_lower:
        return 0.0
    if _has_negation(text_lower, keyword):
        return -10.0

    idx = text_lower.find(keyword)
    window = text_lower[max(0, idx - 80): idx + len(keyword) + 80]
    if any(sig in window for sig in _REQUEST_SIGNALS):
        return 2.5
    return 1.0


def detect_direction(text: str) -> str:
    """
    Определяет нишу вакансии по ключевым словам в тексте.
    Учитывает контекст: отрицания снижают счёт, сигналы запроса — повышают.
    """
    text_lower = text.lower()
    scores: Dict[str, float] = {}
    for direction, keywords in DIRECTION_KEYWORDS.items():
        score = sum(_score_keyword(text_lower, kw) for kw in keywords)
        if score > 0:
            scores[direction] = score
    if scores:
        return max(scores, key=scores.get)
    return "Unknown"


