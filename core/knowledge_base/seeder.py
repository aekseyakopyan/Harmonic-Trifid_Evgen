async def seed_if_empty(db_path: str) -> None:
    """Заполняет services и cases, если таблицы пустые. Идемпотентно."""
    async with aiosqlite.connect(db_path) as db:
        cursor = await db.execute("SELECT COUNT(*) FROM cases")
        row = await cursor.fetchone()
        if row and row[0] > 0:
            logger.debug("Seeder: cases уже заполнены, пропуск.")
            return

        now = datetime.now(timezone.utc).isoformat()

        for s in SERVICES:
            await db.execute(
                "INSERT INTO services (name, description, price_range, process, timeline, created_at, updated_at) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                (s["name"], s["description"], s["price_range"], s["process"], s["timeline"], now, now),
            )

        for c in SEO_CASES + CONTEXT_CASES:
            await db.execute(
                "INSERT INTO cases (title, category, description, results, is_active, created_at, updated_at) "
                "VALUES (?, ?, ?, ?, 1, ?, ?)",
                (c["title"], c["category"], c["description"], c["results"], now, now),
            )

        await db.commit()
        logger.info(f"Seeder: вставлено {len(SERVICES)} услуг и {len(SEO_CASES) + len(CONTEXT_CASES)} кейсов.")
